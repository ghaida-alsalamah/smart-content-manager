const i18n = {
  current: localStorage.getItem('scm_lang') || 'en',
  afterApply: null,

  t(key, vars) {
    let val = translations[this.current][key] || translations['en'][key] || key;
    if (vars) Object.keys(vars).forEach(k => { val = val.split('{' + k + '}').join(vars[k]); });
    return val;
  },

  apply() {
    const lang = this.current;
    document.documentElement.setAttribute('lang', lang);
    document.body.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const attr = el.getAttribute('data-i18n-attr');
      const val  = this.t(key);
      if (attr) { el.setAttribute(attr, val); }
      else { el.textContent = val; }
    });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      el.innerHTML = this.t(key);
    });

    // Update lang button label
    const btn = document.getElementById('langBtn');
    if (btn) btn.textContent = lang === 'ar' ? 'EN' : 'عر';

    localStorage.setItem('scm_lang', lang);
    if (this.afterApply) this.afterApply();
  },

  toggle() {
    this.current = this.current === 'ar' ? 'en' : 'ar';
    this.apply();
  },

  init() {
    this.apply();
    const btn = document.getElementById('langBtn');
    if (btn) btn.addEventListener('click', () => this.toggle());
  }
};
